int main(void) {

	HAL_Init();

	SystemClock_Config();

	MX_GPIO_Init();
	//MX_USART1_UART_Init();
	MX_DMA_Init();
	MX_TIM1_Init();
	LED_AnimationConfig_Init();

	__HAL_TIM_ENABLE_DMA(&htim1, TIM_DMA_UPDATE);
	__HAL_TIM_ENABLE(&htim1);

	HAL_DMA_RegisterCallback(htim1.hdma[TIM_DMA_ID_UPDATE],
			HAL_DMA_XFER_CPLT_CB_ID, &DMATransmitCplt);

	DMA_StartTransmition();

	iteration_counter = 0; //FIXME: Use timer instead

	while (1) {
		HAL_Delay(0);
		while (!transfer_is_completed)
			;
		LED_Animation_Step(&leds_config);
		DMA_StartTransmition();
		//HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_3);
		if (iteration_counter++ == 1000) {
			LED_Switch_Mode(&leds_config);
			DMA_StartTransmition();
			iteration_counter = 0;
		}
	}
}

