void DMATransmitCplt(DMA_HandleTypeDef *hdma) {
	if (!transfer_is_completed) {
		uint8_t pck_pointer = 0;
		uint32_t p = 0x00800000;
		uint32_t value = leds_config.buff[leds_buff_pointer];
		for (int i = 0; i < 24; i++) {
			if ((p & value) == p) {
				package_buff[pck_pointer] = 0xFFFF;
				package_buff[pck_pointer + 1] = 0xFFFF;
				package_buff[pck_pointer + 2] = 0xFFFF;
				package_buff[pck_pointer + 3] = 0x0000;
			} else {
				package_buff[pck_pointer] = 0xFFFF;
				package_buff[pck_pointer + 1] = 0x0000;
				package_buff[pck_pointer + 2] = 0x0000;
				package_buff[pck_pointer + 3] = 0x0000;
			}
			pck_pointer += 4;
			p = p >> 1;
		}

		HAL_DMA_Start_IT(htim1.hdma[TIM_DMA_ID_UPDATE], (uint32_t) package_buff,
				(uint32_t) &GPIOB->ODR, 96);
		__HAL_TIM_SET_COUNTER(&htim1, 0);

		leds_buff_pointer++;
		if (leds_buff_pointer == leds_config.leds_number)
			transfer_is_completed = 1;
	}
}

void DMA_StartTransmition() {
	transfer_is_completed = 0;
	leds_buff_pointer = 0;
	DMATransmitCplt(htim1.hdma[TIM_DMA_ID_UPDATE]);
}

void LED_AnimationConfig_Init(void) {
	leds_config.buff = &leds_buff;
	leds_config.leds_number = 250;
	LED_Animation_Init(&leds_config);
}

